//Type coercion - JS compiler or engine converts one data type to other type automatically

// + sign addition me tb use hoga jab dono side numbers honge otherwise concatenation ke liye use hoga

console.log("1" + 1); // "11"
console.log("1" - 1); // 0
console.log("1" * 1); // 1
console.log("1" / 1); // 1
console.log(true + 1); //2
console.log(false + 1); //1
console.log(null + 1); //1
console.log(undefined + 1); //NaN
